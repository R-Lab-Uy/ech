  expect_equal(nrow(b), 19)
  b <- get_estimation_mean(data = df, variable = "ht11", by.x = "dpto", by.y = "mes", level = "h")
  expect_equal(nrow(b), 149)
  b <- get_estimation_mean(data = df, variable = "ht11", by.x = "secc", by.y = "mes", domain = "dpto == 1", level = "i")
